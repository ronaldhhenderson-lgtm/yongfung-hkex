/* ----- Apply translations ----- */
function applyI18n(lang) {
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : (lang === 'tc' ? 'zh-HK' : 'en');
  document.body.classList.remove('lang-en', 'lang-zh', 'lang-tc');
  document.body.classList.add('lang-' + lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const entry = I18N[key];
    if (!entry) { return; }
    el.textContent = entry[lang] || entry.en;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    const entry = I18N[key];
    if (!entry) { return; }
    el.innerHTML = entry[lang] || entry.en;
  });
  document.querySelectorAll('[data-i18n-attr]').forEach(el => {
    const spec = el.getAttribute('data-i18n-attr');
    spec.split(',').forEach(pair => {
      const [attr, key] = pair.trim().split(':');
      const entry = I18N[key];
      if (!entry) { return; }
      el.setAttribute(attr, entry[lang] || entry.en);
    });
  });

  try { localStorage.setItem('yf_lang', lang); } catch (e) {}

  document.querySelectorAll('.nav-lang button').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });

  const titleEl = document.querySelector('title[data-i18n]');
  if (titleEl) {
    const key = titleEl.getAttribute('data-i18n');
    const entry = I18N[key];
    if (entry) { document.title = entry[lang] || entry.en; }
  }
}

function initLang() {
  let lang = 'en';
  try { lang = localStorage.getItem('yf_lang') || 'en'; } catch (e) {}
  if (!['en','zh','tc'].includes(lang)) lang = 'en';
  applyI18n(lang);

  document.querySelectorAll('.nav-lang button').forEach(btn => {
    btn.addEventListener('click', () => applyI18n(btn.dataset.lang));
  });

  const toggle = document.querySelector('.nav-toggle');
  const links = document.querySelector('.nav-links');
  if (toggle && links) {
    toggle.addEventListener('click', () => links.classList.toggle('open'));
  }
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLang);
} else {
  initLang();
}
